import { Router, Request, Response, NextFunction } from "express";
import { UsersController } from "../controllers/users.controller";
import { authenticateJWT } from "../../middlewares/auth.middleware";

const router = Router();

/**
 * Blocks the route in production.
 * Account management is done manually (DB / dev env) â€” no public signup/delete.
 */
const blockInProduction = (_req: Request, res: Response, next: NextFunction) => {
  if (process.env.NODE_ENV === "production") {
    res.status(403).json({ error: "This action is disabled in production" });
    return;
  }
  next();
};

/**
 * @swagger
 * tags:
 *   name: Users
 *   description: User management
 */

/**
 * @swagger
 * /users:
 *   post:
 *     summary: Create a new user
 *     tags: [Users]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *               email:
 *                 type: string
 *     responses:
 *       201:
 *         description: User created
 */
router.post("/users", blockInProduction, UsersController.create);

/**
 * @swagger
 * /users:
 *   get:
 *     summary: Get all users
 *     tags: [Users]
 *     responses:
 *       200:
 *         description: List of users
 */
router.get("/users", authenticateJWT, UsersController.getAll);

/**
 * @swagger
 * /users/{id}:
 *   get:
 *     summary: Get user by ID
 *     tags: [Users]
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: integer
 *     responses:
 *       200:
 *         description: User found
 *       404:
 *         description: User not found
 */
router.get("/users/:id", authenticateJWT, UsersController.getById);

/**
 * @swagger
 * /users/{id}:
 *   put:
 *     summary: Update user
 *     tags: [Users]
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: integer
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *               email:
 *                 type: string
 *     responses:
 *       200:
 *         description: User updated
 */
router.put("/users/:id", authenticateJWT, blockInProduction, UsersController.update);

/**
 * @swagger
 * /users/{id}:
 *   delete:
 *     summary: Delete user
 *     tags: [Users]
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: integer
 *     responses:
 *       204:
 *         description: User deleted
 */
router.delete("/users/:id", authenticateJWT, blockInProduction, UsersController.delete);


router.post("/login", UsersController.loginUser);

export default router;